SUBROUTINE LEGACY_FIELD_5IM (SELF, KADDRL, KADDRU, KDIR)

USE FIELD_5IM_ACCESS_MODULE
USE FIELD_CONSTANTS_MODULE

CLASS (FIELD_5IM), POINTER :: SELF
INTEGER*8, INTENT (IN) :: KADDRL
INTEGER*8, INTENT (IN) :: KADDRU
INTEGER,   INTENT (IN) :: KDIR

INTEGER(KIND=JPIM), POINTER :: PTR (:,:,:,:,:), DEVPTR (:,:,:,:,:)
INTEGER*8 :: IADDRL
INTEGER*8 :: IADDRU
INTEGER (KIND=JPIM) :: ILBOUNDS (5)
INTEGER (KIND=JPIM) :: IUBOUNDS (5)

IF (ASSOCIATED (SELF)) THEN

  CALL SELF%GET_DIMS (LBOUNDS=ILBOUNDS, UBOUNDS=IUBOUNDS)

  PTR => SELF%PTR

  IADDRL = LOC (PTR (ILBOUNDS(1),ILBOUNDS(2),ILBOUNDS(3),ILBOUNDS(4),ILBOUNDS(5)))
  IADDRU = LOC (PTR (IUBOUNDS(1),IUBOUNDS(2),IUBOUNDS(3),IUBOUNDS(4),IUBOUNDS(5)))

  IF ((KADDRL <= IADDRL) .AND. (IADDRU <= KADDRU)) THEN

    IF (KDIR == NF2L) THEN
      DEVPTR => GET_DEVICE_DATA_RDONLY (SELF)
      CALL LEGACY_FIELD_5IM_ASSIGN (PTR, DEVPTR)
    ELSEIF (KDIR == NL2F) THEN
      DEVPTR => GET_DEVICE_DATA_RDWR (SELF)
      CALL LEGACY_FIELD_5IM_ASSIGN (DEVPTR, PTR)
    ENDIF

  ENDIF

ENDIF

CONTAINS

SUBROUTINE LEGACY_FIELD_5IM_ASSIGN (PTR_RHS, PTR_LHS)

INTEGER(KIND=JPIM) :: PTR_RHS (:,:,:,:,:), PTR_LHS (:,:,:,:,:)


PTR_RHS = PTR_LHS


END SUBROUTINE

END SUBROUTINE 


