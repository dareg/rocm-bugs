SUBROUTINE LEGACY_FIELD_1RD (SELF, KADDRL, KADDRU, KDIR)

USE FIELD_1RD_ACCESS_MODULE
USE FIELD_CONSTANTS_MODULE

CLASS (FIELD_1RD), POINTER :: SELF
INTEGER*8, INTENT (IN) :: KADDRL
INTEGER*8, INTENT (IN) :: KADDRU
INTEGER,   INTENT (IN) :: KDIR

REAL(KIND=JPRD), POINTER :: PTR (:), DEVPTR (:)
INTEGER*8 :: IADDRL
INTEGER*8 :: IADDRU
INTEGER (KIND=JPIM) :: ILBOUNDS (1)
INTEGER (KIND=JPIM) :: IUBOUNDS (1)

IF (ASSOCIATED (SELF)) THEN

  CALL SELF%GET_DIMS (LBOUNDS=ILBOUNDS, UBOUNDS=IUBOUNDS)

  PTR => SELF%PTR

  IADDRL = LOC (PTR (ILBOUNDS(1)))
  IADDRU = LOC (PTR (IUBOUNDS(1)))

  IF ((KADDRL <= IADDRL) .AND. (IADDRU <= KADDRU)) THEN

    IF (KDIR == NF2L) THEN
      DEVPTR => GET_DEVICE_DATA_RDONLY (SELF)
      CALL LEGACY_FIELD_1RD_ASSIGN (PTR, DEVPTR)
    ELSEIF (KDIR == NL2F) THEN
      DEVPTR => GET_DEVICE_DATA_RDWR (SELF)
      CALL LEGACY_FIELD_1RD_ASSIGN (DEVPTR, PTR)
    ENDIF

  ENDIF

ENDIF

CONTAINS

SUBROUTINE LEGACY_FIELD_1RD_ASSIGN (PTR_RHS, PTR_LHS)

REAL(KIND=JPRD) :: PTR_RHS (:), PTR_LHS (:)


PTR_RHS = PTR_LHS


END SUBROUTINE

END SUBROUTINE 


